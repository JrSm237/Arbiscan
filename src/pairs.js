// Score de priorité dynamique
const pairScores = new Map();

// Toutes les paires combinées (1000+)
const ALL_PAIRS = [...new Set([
  ...TIER1, ...TIER2, ...TIER3, ...TIER4, ...TIER5, ...TIER6, ...TIER7, ...TIER8, ...TIER9, ...TIER10, ...TIER11
])];

// Retourne les paires triées par score de priorité
function getPrioritizedPairs(limit) {
  const lim = limit || ALL_PAIRS.length;
  return [...ALL_PAIRS]
    .sort((a, b) => (pairScores.get(b) || 0) - (pairScores.get(a) || 0))
    .slice(0, lim);
}

// Booste la priorité d'une paire qui a montré un spread
function boostPair(symbol, spreadPct) {
  pairScores.set(symbol, (pairScores.get(symbol) || 0) + spreadPct);
}

