const ALL_PAIRS = [...new Set([...T1, ...T2, ...T3, ...T4, ...TEXTRA, ...TEXTRA2, ...TEXTRA3, ...TEXTRA4, ...T5, ...T6])];

const _scores = new Map();

function getPrioritizedPairs(limit) {
  const lim = limit || ALL_PAIRS.length;
  const topSet = new Set(TOP);
  const rest = ALL_PAIRS.filter(p => !topSet.has(p))
    .sort((a,b) => (_scores.get(b)||0) - (_scores.get(a)||0));
  return [...TOP, ...rest].slice(0, lim);
}

function boostPair(symbol, spreadPct) {
  _scores.set(symbol, (_scores.get(symbol)||0) + spreadPct);
}

module.exports = { ALL_PAIRS, TOP_PAIRS: TOP, getPrioritizedPairs, boostPair };
